public class Method4 {
// final keyword :- we can make a variable  behave as a constant variables by prefixing the keyword called as final
    public static void main(String[] args) {
        System.out.println("start");
        final int a = 100;  // final value cannot be change
        int b = 200;
        System.out.println("a is "+a);
    }
}
