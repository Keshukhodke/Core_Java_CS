package For_Loop_1_09_2025;

//Print a Reverse Number Triangle

public class Nested_Loop7 {
    public static void main(String[] args) {
        for (int i = 5; i>=1; i--)
        {
            for (int j = 1; j<=i; j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
