package For_Loop_1_09_2025;

public class For2 {
    public static void main(String[] args) {
        // Start of an infinite for loop.
        // The loop has no initialization, condition, or increment statement,
        // so it runs forever unless manually stopped.
        for (;;)  // infinite loop because no condition is given
        {
            // Print "hello " to the console on each loop iteration
            System.out.println("hello ");
        }
        // This line is never reached, as the loop is infinite
    }
}
