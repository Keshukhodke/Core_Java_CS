package Do_While_02_09_25;

public class Do_While_Loop1 {
    public static void main(String[] args) {
        System.out.println("start");
        int i =5;
        do {
            System.out.println("insidide do while :- "+i);
            i++;
        }
        while (i<=5);
        System.out.println("stop");
    }
}
