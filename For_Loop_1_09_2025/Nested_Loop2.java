package For_Loop_1_09_2025;

public class Nested_Loop2 {
    public static void main(String[] args) {
        for (int i =1; i<=3; i++)
        {
            for (int j =1; j<=i; j++)
            {
                System.out.print(j);
            }
            System.out.println();
        }
    }
}
